#include <bits/stdc++.h>
 
using namespace std;
 
void solve() {
    int n, s, div, qnt;
    cin >> n >> s;
    div = n/2 + 1;
    qnt = s/div;
    cout << qnt << endl;
 
return;
}
 
signed main(){
    ios_base::sync_with_stdio(false); 
    cin.tie(0); cout.tie(0);
    int t = 1;  
    cin >> t;
    while(t--) solve();
    return 0;
}
